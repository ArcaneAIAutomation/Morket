/**
 * Sanitize HTML content by escaping dangerous characters.
 * Prevents XSS when rendering user-provided content in the DOM.
 */
export function sanitizeHtml(input: string): string {
  return input
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#x27;');
}
